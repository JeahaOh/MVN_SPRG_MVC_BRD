-- 스키마 생성
CREATE DATABASE board;

-- TB_BOARD 테이블 삭제
DROP TABLE board.TB_BOARD;

-- TB_BOARD 테이블 생성 및 코멘트 추가
CREATE TABLE board.TB_BOARD (
  BOARD_SEQ INT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '게시글 번호',
  BOARD_RE_REF INT COMMENT '글 그룹 번호',
  BOARD_RE_LEV INT COMMENT '답변 글의 깊이',
  BOARD_RE_SEQ INT COMMENT '답변 글의 순서',
  BOARD_WRITER VARCHAR(20) COMMENT '게시글 작성자',
  BOARD_SUBJECT VARCHAR(50) COMMENT '게시글 제목',
  BOARD_CONTENT VARCHAR(2000) COMMENT '게시글 내용',
  BOARD_HITS INT UNSIGNED NOT NULL DEFAULT 0 COMMENT '게시글 조회수',
  DEL_YN VARCHAR(1) NOT NULL DEFAULT 'N' COMMENT '삭제 유무',
  INS_USER_ID VARCHAR(20) COMMENT '입력자 ID',
  INS_DATE DATETIME COMMENT '입력 일시',
  UPD_USER_ID VARCHAR(20) COMMENT '수정자 ID',
  UPD_DATE DATETIME COMMENT '수정 일시',
  PRIMARY KEY ( BOARD_SEQ )
) COMMENT '게시판';

